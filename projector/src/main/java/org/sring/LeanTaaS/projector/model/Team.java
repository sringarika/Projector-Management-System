package org.sring.LeanTaaS.projector.model;

public class Team {

    private int id;

    public Team() {
        
    }
    
    public Team(int id) {
        this.id = id;
    }
    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }
}
